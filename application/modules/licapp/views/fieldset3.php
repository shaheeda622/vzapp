//        document.getElementById('selected_act').innerHTML = '';
//      }

      $('.activity').click(function(){
//        if($('#activities').find('li').length === 1){
//          document.getElementById('selected_act').innerHTML = '';
//        }
        $(this).remove();
        updateActivitesInput();
      });

    });

