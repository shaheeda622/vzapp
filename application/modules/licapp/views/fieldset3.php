  <script type="text/javascript">
    var updateActivitesInput = function(){
      var acts = [];
      $('#activities li').each(function(){
        acts.push($(this).data('text'));
      });
      $('input[name="question5"]').val(acts.join(','));
    };
    $('#myModal').on('show.bs.modal', function(event){
      $('span.merror').hide();
      var button = $(event.relatedTarget);
      var modal = $(this);
      modal.find('.modal-title1').text(button.find('p').text());
      modal.find('.modal-body').html(button.find('select').clone());
      modal.find('.modal-body select').show();
    });
    $('#ok_btn').click(function(){
      var val = $('.modal-body select').val();
      if(val === ''){
        return;
      }
      if($('#activities').find('li').length >= 6){
        $('span.merror').html('Selection of up to six business activities from different activity groups is permitted.');
        $('span.merror').show();
        return;
      }

      if($('#activities').text().indexOf(val) >= 0){
        $('span.merror').html('Activity already selected. Please select another one.');
        $('span.merror').show();
        return;
      }
      $('#activities').append('<li class="activity" style="list-style:none;" data-text="' + val + '"><span><img src="<?php echo url(); ?>licappres/assets/images/remove-icon.png" style="cursor:pointer;" width="13" height="13" alt="icon" align="top" /></span> &nbsp;' + val + '</li>');
      updateActivitesInput();

//      if($('#activities').find('li').length > 0){
